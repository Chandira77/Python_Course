list1 = ["apple", "banana", "mango","strawberry", "pear", "watermelon"]
print(list1[1]) 
print(list1[0]) # The first item has index 0
print(list1[-1]) # negative indexing means start from end
print(list1[1:4])  # range of indexing. second, third, fourth item is returned
print(list1[:5]) # this will return the item from index 0  to index 4 total 5 item
print(list1[2:]) # this will return the item from index 2 to index last
print(list1[-3 : -1])

